import sys

def dijkstra_algorithm(G, S, T):
    L=[]
    n=len(G)

    for i in range(len(T)):
        nodes_path = []
        distance = [sys.maxsize] * n
        used = [False] * n 
        previous = [None] * n 
        
        distance[S] = 0

        while True:
            min_distance = sys.maxsize #infinity
            min_node = None
            
            for m in range(n):
                if not used[m] and distance[m] < min_distance:
                    min_distance = distance[m]
                    min_node = m

            if min_distance == sys.maxsize:
                break  

            used[min_node] = True  

            
            for l in range(n):
                if G[min_node][l] > 0:
                    shortest_to_min_node = distance[min_node]
                    distance_to_next_node = G[min_node][l]
                    total_distance = shortest_to_min_node + distance_to_next_node
                    
                    
                    if total_distance < distance[l]:
                        distance[l] = total_distance
                        previous[l] = min_node

        
        if distance[T[i]] == sys.maxsize: 
            return None  

        current_node = T[i]
        while current_node is not None: 
            nodes_path.append(current_node) 
            current_node = previous[current_node] 
        
        
        col1 = S 
        for j in range(len(nodes_path) - 1, -1, -1): 
            L.append(nodes_path[j])  
            col1 = nodes_path[j]

        S = T[i]  

    return L 



